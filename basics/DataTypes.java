// Java Data Types Example
// This program demonstrates different data types available in Java.

public class DataTypes {

    public static void main(String[] args) {

        /*
         * Data types define what kind of value
         * a variable can store.
         */

        // int stores whole numbers
        int number = 100;

        // double stores decimal values
        double price = 99.99;

        // char stores a single character
        char letter = 'J';

        // boolean stores true or false
        boolean isJavaEasy = true;

        // String stores text
        String message = "Learning Java";

        // Printing all values
        System.out.println("Integer Value: " + number);
        System.out.println("Double Value: " + price);
        System.out.println("Character Value: " + letter);
        System.out.println("Boolean Value: " + isJavaEasy);
        System.out.println("String Value: " + message);

        // Program completion message
        System.out.println("Data types example completed.");
    }
}