package methods;

// Java Method Basics Example
// This program demonstrates basic methods in Java.

public class MethodBasics {

    /*
     * A method is a block of code
     * that performs a specific task.
     */

    // Creating a method
    static void greetUser() {

        System.out.println("Welcome to Java Methods!");
    }

    public static void main(String[] args) {

        /*
         * Calling the method.
         */

        greetUser();

        System.out.println("Method basics example completed.");
    }
}